package com.strawberryswing.upmusic.requestModel;

public class Token {
    private String token;

    public Token() {
        token = "";
    }

    public Token(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
